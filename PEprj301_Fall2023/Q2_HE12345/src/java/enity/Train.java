package enity;

/**
 *
 * @author HP
 */
public class Train {
    private String code, name, number;

    public Train() {
    }

    public Train(String code, String name, String number) {
        this.code = code;
        this.name = name;
        this.number = number;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }
    
    
    
}
